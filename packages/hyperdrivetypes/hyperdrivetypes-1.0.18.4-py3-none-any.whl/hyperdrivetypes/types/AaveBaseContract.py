    ReentrancyGuardReentrantCall: (
        AaveBaseReentrancyGuardReentrantCallContractError
    )

    SafeERC20FailedOperation: AaveBaseSafeERC20FailedOperationContractError

    def __init__(
        self,
    ) -> None:
        self.AddressEmptyCode = AaveBaseAddressEmptyCodeContractError()
        self.AddressInsufficientBalance = (
            AaveBaseAddressInsufficientBalanceContractError()
        )
        self.FailedInnerCall = AaveBaseFailedInnerCallContractError()
        self.ReentrancyGuardReentrantCall = (
            AaveBaseReentrancyGuardReentrantCallContractError()
        )
        self.SafeERC20FailedOperation = (
            AaveBaseSafeERC20FailedOperationContractError()
        )

        self._all = [
            self.AddressEmptyCode,
            self.AddressInsufficientBalance,
            self.FailedInnerCall,
            self.ReentrancyGuardReentrantCall,
            self.SafeERC20FailedOperation,
        ]

    def decode_custom_error(self, data: str) -> tuple[Any, ...]:
        """Decodes a custom contract error."""
        selector = data[:10]
        for err in self._all:
            if err.selector == selector:
                return err.decode_error_data(HexBytes(data[10:]))

        raise ValueError(
            f"AaveBase does not have a selector matching {selector}"
        )



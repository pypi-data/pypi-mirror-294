class HyperdriveContractErrors:
    """ContractErrors for the Hyperdrive contract."""

    ReentrancyGuardReentrantCall: (
        HyperdriveReentrancyGuardReentrantCallContractError
    )

    UnexpectedSuccess: HyperdriveUnexpectedSuccessContractError

    def __init__(
        self,
    ) -> None:
        self.ReentrancyGuardReentrantCall = (
            HyperdriveReentrancyGuardReentrantCallContractError()
        )
        self.UnexpectedSuccess = HyperdriveUnexpectedSuccessContractError()

        self._all = [
            self.ReentrancyGuardReentrantCall,
            self.UnexpectedSuccess,
        ]

    def decode_custom_error(self, data: str) -> tuple[Any, ...]:
        """Decodes a custom contract error."""
        selector = data[:10]
        for err in self._all:
            if err.selector == selector:
                return err.decode_error_data(HexBytes(data[10:]))

        raise ValueError(
            f"Hyperdrive does not have a selector matching {selector}"
        )



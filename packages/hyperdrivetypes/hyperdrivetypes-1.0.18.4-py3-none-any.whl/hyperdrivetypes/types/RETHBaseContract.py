class RETHBaseContractErrors:
    """ContractErrors for the RETHBase contract."""

    ReentrancyGuardReentrantCall: (
        RETHBaseReentrancyGuardReentrantCallContractError
    )

    TransferFailed: RETHBaseTransferFailedContractError

    def __init__(
        self,
    ) -> None:
        self.ReentrancyGuardReentrantCall = (
            RETHBaseReentrancyGuardReentrantCallContractError()
        )
        self.TransferFailed = RETHBaseTransferFailedContractError()

        self._all = [
            self.ReentrancyGuardReentrantCall,
            self.TransferFailed,
        ]

    def decode_custom_error(self, data: str) -> tuple[Any, ...]:
        """Decodes a custom contract error."""
        selector = data[:10]
        for err in self._all:
            if err.selector == selector:
                return err.decode_error_data(HexBytes(data[10:]))

        raise ValueError(
            f"RETHBase does not have a selector matching {selector}"
        )


